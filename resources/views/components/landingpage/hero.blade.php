<script>
    document.addEventListener('DOMContentLoaded', function() {
        const swiper = new Swiper(".bgSwiper", {
            loop: true,
            autoplay: {
                delay: 5000,
                disableOnInteraction: false,
            },
            effect: 'fade',
            navigation: {
                nextEl: '#nextBtn',
                prevEl: '#prevBtn',
            },
            on: {
                slideChange: function() {
                    const activeSlide = swiper.slides[swiper.activeIndex];
                    const title = activeSlide.getAttribute('data-title');

                    document.querySelectorAll('.slide-content').forEach(el => {
                        el.classList.add('hidden');
                        if (el.getAttribute('data-title') === title) {
                            el.classList.remove('hidden');
                        }
                    });

                    document.querySelectorAll('.menu-label').forEach(el => {
                        if (el.getAttribute('data-title') === title) {
                            el.classList.remove('text-white');
                            el.classList.add('text-[#FFB71A]');
                        } else {
                            el.classList.add('text-white');
                            el.classList.remove('text-[#FFB71A]');
                        }
                    });
                }
            }
        });


        swiper.emit('slideChange');
    });
</script>
